// Each skill is a self-contained agent that can run on a schedule or trigger.

import { getActiveProvider, complete } from "@/lib/llm";

export type SkillStatus = "active" | "paused" | "error" | "running";
export type SkillTrigger = "manual" | "scheduled" | "event";
export type SkillCategory = "revenue" | "research" | "outreach" | "monitoring" | "reporting" | "automation";

export interface SkillConfig {
  [key: string]: string | number | boolean | string[];
}

export interface SkillRun {
  id: string;
  skillId: string;
  startedAt: string;
  completedAt?: string;
  status: "running" | "success" | "failed";
  output?: string;
  actions?: SkillAction[];
  error?: string;
  tokensUsed?: number;
  costUSD?: number;
}

export interface SkillAction {
  type: "draft" | "alert" | "research" | "submit" | "email" | "telegram";
  title: string;
  description: string;
  data?: object;
  requiresApproval: boolean;
  approved?: boolean;
  executedAt?: string;
}

export interface Skill {
  id: string;
  name: string;
  description: string;
  category: SkillCategory;
  version: string;
  author: string;
  status: SkillStatus;
  trigger: SkillTrigger;
  schedule?: string; // "daily_6am" | "weekly_sunday" | "hourly" etc
  config: SkillConfig;
  lastRun?: string;
  nextRun?: string;
  totalRuns: number;
  successRuns: number;
  totalRevenue: number; // USD generated by this skill
  installedAt: string;
  isBuiltIn: boolean;
}

