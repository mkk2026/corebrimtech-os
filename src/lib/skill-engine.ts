const ANTHROPIC_KEY_STORAGE = "cbt_os_anthropic_api_key";

export function getStoredAnthropicKey(): string | undefined {
  if (typeof window === "undefined") return undefined;
  const k = localStorage.getItem(ANTHROPIC_KEY_STORAGE);
  return (k && k.trim() && k !== "your_claude_api_key") ? k : undefined;
}

export async function executeSkill(
  skillId: string,
  apiKey?: string,
  telegramToken?: string,
  telegramChatId?: string
): Promise<SkillRun> {
  const key = apiKey ?? getStoredAnthropicKey();
  const skill = getSkill(skillId);
  if (!skill) throw new Error(`Skill ${skillId} not found`);

  const runId = `run_${Date.now()}`;
  const run: SkillRun = {
    id: runId,
    skillId,
    startedAt: new Date().toISOString(),
    status: "running",
    actions: [],
  };

  logSkillRun(run);
  updateSkill(skillId, { status: "running" as SkillStatus, lastRun: new Date().toISOString() });

  try {
    let result: Partial<SkillRun> = {};

    switch (skillId) {
      case "skill_proposal_writer":
        result = await runProposalWriter(skill, key);
        break;
      case "skill_grant_drafter":
        result = await runGrantDrafter(skill, key);
        break;
      case "skill_hackathon_auto_builder":
        result = await runHackathonAutoBuilder(skill, key);
        break;
      case "skill_competitor_monitor":
        result = await runCompetitorMonitor(skill, key);
        break;
      case "skill_opportunity_scanner":
        result = await runOpportunityScanner(skill, key);
        break;
      case "skill_weekly_report":
        result = await runWeeklyReport(skill, key);
        break;
      case "skill_lead_outreach":
        result = await runLeadOutreach(skill, key);
        break;
      case "skill_away_mode":
        result = await runAwayMode(skill, key, telegramToken, telegramChatId);
        break;
      default:
        result = { output: "Unknown skill", status: "failed" };
    }

    const finalRun: SkillRun = {
      ...run,
      ...result,
      completedAt: new Date().toISOString(),
      status: result.status || "success",
    };

    logSkillRun(finalRun);
    updateSkill(skillId, {
      status: skill.status === "running" ? "active" : skill.status,
      totalRuns: skill.totalRuns + 1,
      successRuns: skill.successRuns + (finalRun.status === "success" ? 1 : 0),
    });

    return finalRun;
  } catch (err) {
    const failedRun: SkillRun = {
      ...run,
      completedAt: new Date().toISOString(),
      status: "failed",
      error: err instanceof Error ? err.message : "Unknown error",
    };
    logSkillRun(failedRun);
    updateSkill(skillId, { status: "error" as SkillStatus });
    return failedRun;
  }
}

