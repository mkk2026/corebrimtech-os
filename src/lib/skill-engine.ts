async function runProposalWriter(skill: Skill, apiKey?: string): Promise<Partial<SkillRun>> {
  const brain = getBrain();
  const clients = getClients().filter(c => c.status === "proposal");
  const active = getActiveProvider();
  const useMock = !active;

  if (clients.length === 0) {
    return { output: "No clients in proposal stage — nothing to draft.", status: "success", actions: [] };
  }

  const actions: SkillAction[] = [];

  for (const client of clients.slice(0, 3)) {
    let proposalText = "";

    if (useMock) {
      proposalText = generateMockProposal(client, brain);
    } else {
      try {
        const prompt = `Write a professional client proposal for:
Client: ${client.name} at ${client.company || "their company"}
Service: ${client.service}
Value: $${client.value}
Source: ${client.source}

Our company: ${brain?.companyName || "Core Brim Tech"}
Our mission: ${brain?.companyMission || "Building impactful tech for emerging markets"}
Our skills: ${brain?.founders[0]?.skills?.join(", ") || "Full-stack development, AI integration"}

Write a compelling 3-paragraph proposal: (1) their problem, (2) our solution, (3) investment and next steps. Professional but warm tone. End with a clear CTA.`;
        proposalText = await complete({ prompt, maxTokens: 2000 });
      } catch {
        proposalText = generateMockProposal(client, brain);
      }
    }

    actions.push({
      type: "draft",
      title: `Proposal for ${client.name}`,
      description: `Draft proposal for ${client.service} — $${client.value}`,
      data: { clientId: client.id, proposal: proposalText },
      requiresApproval: true,
    });
  }

  return {
    status: "success",
    output: `Drafted ${actions.length} proposal(s) for review.`,
    actions,
  };
}

