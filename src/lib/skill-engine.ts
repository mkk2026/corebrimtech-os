// ── STORAGE ───────────────────────────────────────────────────────────────────

const KEYS = {
  skills: "cbt_os_skills",
  runs: "cbt_os_skill_runs",
  queue: "cbt_os_skill_queue",
};

function load<T>(key: string): T[] {
  if (typeof window === "undefined") return [];
  try { return JSON.parse(localStorage.getItem(key) || "[]"); } catch { return []; }
}

function persist<T>(key: string, data: T[]): void {
  if (typeof window === "undefined") return;
  localStorage.setItem(key, JSON.stringify(data));
}

export function initSkills(): void {
  if (typeof window === "undefined") return;
  const existing = load<Skill>(KEYS.skills);
  if (existing.length > 0) return;

  const skills: Skill[] = BUILT_IN_SKILLS.map(s => ({
    ...s,
    installedAt: new Date().toISOString(),
    totalRuns: 0,
    successRuns: 0,
    totalRevenue: 0,
  }));
  persist(KEYS.skills, skills);
}

export function getSkills(): Skill[] {
  initSkills();
  return load<Skill>(KEYS.skills);
}

export function getSkill(id: string): Skill | null {
  return getSkills().find(s => s.id === id) || null;
}

export function updateSkill(id: string, updates: Partial<Skill>): void {
  const skills = getSkills();
  const idx = skills.findIndex(s => s.id === id);
  if (idx >= 0) {
    skills[idx] = { ...skills[idx], ...updates };
    persist(KEYS.skills, skills);
  }
}

export function toggleSkill(id: string): void {
  const skill = getSkill(id);
  if (!skill) return;
  updateSkill(id, { status: skill.status === "active" ? "paused" : "active" });
}

// ── RUN HISTORY ───────────────────────────────────────────────────────────────

export function getSkillRuns(skillId?: string): SkillRun[] {
  const runs = load<SkillRun>(KEYS.runs);
  return skillId ? runs.filter(r => r.skillId === skillId) : runs;
}

export function logSkillRun(run: SkillRun): void {
  const runs = load<SkillRun>(KEYS.runs);
  runs.unshift(run);
  persist(KEYS.runs, runs.slice(0, 200)); // keep last 200 runs
}

export function updateSkillRun(id: string, updates: Partial<SkillRun>): void {
  const runs = load<SkillRun>(KEYS.runs);
  const idx = runs.findIndex(r => r.id === id);
  if (idx >= 0) {
    runs[idx] = { ...runs[idx], ...updates };
    persist(KEYS.runs, runs);
  }
}

// ── ACTION QUEUE (things awaiting approval) ───────────────────────────────────

export function getPendingActions(): (SkillAction & { runId: string; skillName: string })[] {
  const runs = getSkillRuns();
  const pending: (SkillAction & { runId: string; skillName: string })[] = [];
  const skills = getSkills();

  for (const run of runs) {
    if (!run.actions) continue;
    const skill = skills.find(s => s.id === run.skillId);
    for (const action of run.actions) {
      if (action.requiresApproval && !action.approved && !action.executedAt) {
        pending.push({ ...action, runId: run.id, skillName: skill?.name || run.skillId });
      }
    }
  }
  return pending;
}

export function approveAction(runId: string, actionTitle: string): void {
  const runs = load<SkillRun>(KEYS.runs);
  const run = runs.find(r => r.id === runId);
  if (!run?.actions) return;
  const action = run.actions.find(a => a.title === actionTitle);
  if (action) {
    action.approved = true;
    action.executedAt = new Date().toISOString();
  }
  persist(KEYS.runs, runs);
}

// ── STATS ─────────────────────────────────────────────────────────────────────

export function getSkillStats() {
  const skills = getSkills();
  const runs = getSkillRuns();
  const pending = getPendingActions();

  return {
    total: skills.length,
    active: skills.filter(s => s.status === "active").length,
    paused: skills.filter(s => s.status === "paused").length,
    totalRuns: runs.length,
    successRuns: runs.filter(r => r.status === "success").length,
    pendingActions: pending.length,
    totalRevenue: skills.reduce((s, sk) => s + sk.totalRevenue, 0),
    lastActivity: runs[0]?.startedAt || null,
  };
}

